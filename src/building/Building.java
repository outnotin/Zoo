public class Building {
    private String buildingZone;
    private String buildingName;
    private String animalInside;

    public String getBuildingZone() {
        return buildingZone;
    }

    public void setBuildingZone(String buildingZone) {
        this.buildingZone = buildingZone;
    }

    public String getBuildingName() {
        return buildingName;
    }

    public void setBuildingName(String buildingName) {
        this.buildingName = buildingName;
    }

    public String getAnimalInside() {
        return animalInside;
    }

    public void setAnimalInside(String animalInside) {
        this.animalInside = animalInside;
    }
}
